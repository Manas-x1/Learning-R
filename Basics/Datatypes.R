#there are 5 datatypes in R
#1. numeric
numeric_var <- 3.14
print(numeric_var)

#2. character
character_var <- "hello world"
print(character_var)

#3. logical
logical_var <- TRUE
print(logical_var)

#4. integer
integer_var <- 10
print(integer_var)

#5. complex
complex_var <- 1 + 2i
print(complex_var)

#these are the most common datatypes in R that you will encounter.